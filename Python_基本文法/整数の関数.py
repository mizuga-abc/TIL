#「max( )」関数、一番大きい数を調べる。
print("「max( )」関数、一番大きい数を調べる。")
max_number = max(2,100,3,55)
print(max_number)


#「max( )」関数、数に変数を使用もできる
print("「max( )」関数、数に変数を使用もできる")
number_a = 5
number_b = 6
number_x = max(number_a,number_b)
print(number_x)

#「min( )」関数、一番小さい数を調べる。数に変数を使用もできる
print("「min( )」関数、一番小さい数を調べる。")
min_number = min(2,100,3,55)
print(min_number)


#「abs( )」関数、絶対値を調べる、「absolute」の略で「絶対」とか「完全」の意味
print("「abs( )」関数、絶対値を調べる、「absolute」の略で「絶対」とか「完全」の意味")
abs_number = abs(222)
print(abs_number)

#「abs( )」に引き算を入れると、その差がいくつなのかを調べられる
print("「abs( )」に引き算を入れると、その差がいくつなのかを調べられる")
abs_numberX = abs(22-222)
print(abs_numberX)
